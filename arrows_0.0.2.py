def one_arrow():
    print("    *")
    print("   * *")
    print("  *   *")
    print(" *     *")
    print("***   ***")
    print("  *   *")
    print("  *   *")
    print("  *****")
    
# Easy way to double my arrows
    
def two_arrows():
    print("    *            " * 2)
    print("   * *           " * 2)
    print("  *   *          " * 2)
    print(" *     *         " * 2)
    print("***   ***        " * 2)
    print("  *   *          " * 2)
    print("  *   *          " * 2)
    print("  *****          " * 2)
    
one_arrow()
two_arrows()